package Day01;

public class EX05_Variable {
	public static void main(String[] args) {
		int x; //int형 변수 x 선언
		int y; //int형 변수 y선언
		// int x , y;
		// int x = 10, y = 20;
		x = 10;
		y = 20;
		System.out.println("x : " + x);
		System.out.println("y : " + y);
		System.out.println("x + y : " + (x + y));
		System.out.println("(x + y) / 2 : " + (x + y) / 2);
		
	}
}
